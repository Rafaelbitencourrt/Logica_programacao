//15 - Exemplos de estruras de repetição.
// exemplo de loop "for" que itera de 0 a 4
for (let i = 0; i < 5; i++) {
  console.log(i);
}

// exemplo de loop "while" que repete até que a condição seja falsa
let i = 0;
while (i < 5) {
  console.log(i);
  i++;
}
// exemplo de loop "do-while" que executa o bloco de código pelo menos uma vez
let i = 0;
do {
  console.log(i);
  i++;
} while (i < 5);
